    private void enrollBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_enrollBtnActionPerformed
        enrolledUser = nameTF.getText();
        mdf = new MDF();
        
        if(enrolledUser.equals(""))
        {
            showMessage("Pemberitahuan", "Kolom nama wajib diisi.", "warning");
        }
        else
        {
            imhandler = new ImageHandler(this.canvas);
            try {
                mdf.extractFeature(imhandler);
                mdf.saveFeatures(enrolledUser);
            } catch (FileNotFoundException ex) {}
            showMessage("Enrollment Berhasil", "Data berhasil disimpan.", "info");
        }
    }//GEN-LAST:event_enrollBtnActionPerformed
    
