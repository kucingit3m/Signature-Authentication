    private void authBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_authBtnActionPerformed
        mdf = new MDF();
        imhandler = new ImageHandler(this.canvas);
        mdf.extractFeature(imhandler);
        
        double[][] features = mdf.getFeatures();
        double minDist = 1000.0;
        
        BufferedReader br = null;
        
        File dirFeature = new File("E:/Signature Dataset/MDF features");
        File[] files = dirFeature.listFiles();
        int n = files.length;
        String result = "";
        
        for(int i=0; i<n; i++)
        {
        try
        {
            StringBuilder content = new StringBuilder();
            try
            {
                String strPerLine = null;
                br = new BufferedReader(new FileReader(files[i].getCanonicalPath()));

                while((strPerLine = br.readLine()) != null)
                {
                    content.append(strPerLine);
                }
            }
            finally
            {
                if(br != null) br.close();
            }
            
            String[] tfdf = content.toString().split(";");
            
            double sum = 0.0;
            double tfTrain;
            double dfTrain;
            double trainFeatures;
            double tfTest;
            double dfTest;
            double testFeatures;
                    
            for(int kk=0; kk<1180; kk++)
            {
                String[] temp = tfdf[n].split("-");
                tfTrain = Double.parseDouble(temp[0]);
                dfTrain = Double.parseDouble(temp[1]);
                trainFeatures = tfTrain + dfTrain;
                
                tfTest = features[0][n];
                dfTest = features[1][n];
                testFeatures = tfTest + dfTest;
                
                sum += (double) (Math.pow((trainFeatures - testFeatures), 2.0));
            }
            
            if(Math.sqrt(sum) <= minDist)
            {
                minDist = Math.sqrt(sum);
                result = files[i].getName();
                result = result.substring(0, result.length()-4);
            }
        }
        catch(IOException ioe)
        {}
        } 
        resultVal.setText(""+result);
    }//GEN-LAST:event_authBtnActionPerformed
    
    private void showMessage(String title, String msg, String type)
    {
        int a = 0;
        switch(type)
        {
            case "error":
                a = JOptionPane.ERROR_MESSAGE;
            break;
            case "warning":
                a = JOptionPane.WARNING_MESSAGE;
            break;
            default:
                a = JOptionPane.INFORMATION_MESSAGE;
            break;
        }
        JOptionPane.showMessageDialog(this, msg, title, a);
    }
    
